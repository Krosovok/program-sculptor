namespace UI.Controls
{
    /// <summary>
    /// Логика взаимодействия для ModelSettings.xaml
    /// </summary>
    public partial class ModelSettings
    {
        public ModelSettings()
        {
            InitializeComponent();
        }

        private void AllowOnlyNumericInput(object sender, TextCompositionEventArgs e)
        {
            e.Handled = !IsTextAllowed(e.Text);
        }

        private static bool IsTextAllowed(string text)
        {
            Regex regex = new Regex("[^0-9]+");
            return !regex.IsMatch(text);
        }

        private void PastingNumbersOnly(object sender, DataObjectPastingEventArgs e)
        {
            if (e.DataObject.GetDataPresent(typeof(string)))
            {
                string text = (string)e.DataObject.GetData(typeof(string));
                if (!IsTextAllowed(text))
                {
                    e.CancelCommand();
                }
            }
            else
            {
                e.CancelCommand();
            }
        }
    }
}
